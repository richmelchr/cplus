public class Emp implements java.io.Serializable {
   private int id;
   private String name;

   public Emp(String n, int i) {
      name = n;
      id = i;
   }

   public String getName() {
      //System.out.println("getName: " + name);
      return name;
   }

   public int getId() {
      return id;
   }
}		
